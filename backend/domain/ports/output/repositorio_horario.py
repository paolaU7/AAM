from typing import Protocol
from datetime import date
from ...models.horario           import Horario
from ...models.excepcion_horario import ExcepcionHorario


class RepositorioHorario(Protocol):
    def porCurso(self, id_curso: str)             -> list[Horario]: ...
    def guardarHorario(self, horario: Horario)    -> None: ...
    def excepcionesPorCurso(
        self,
        id_curso: str,
        fecha:    date | None,
    ) -> list[ExcepcionHorario]: ...
    def guardarExcepcion(self, exc: ExcepcionHorario) -> None: ...
    def actualizarExcepcion(self, exc: ExcepcionHorario) -> None: ...
    def eliminarExcepcion(self, id: str)               -> None: ...
